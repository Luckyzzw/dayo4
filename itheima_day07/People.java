package com.itheima_day07;

public class People {
	private String name;
	private int age;
	//©у╡н
	public People() {
		super();
	}
	//сп╡н
	public People(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	

}
